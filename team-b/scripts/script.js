// #page-topをクリックした際の設定(ページトップに戻る動き)
$('#page-top').click(function () {
  $('body,html').animate({
      scrollTop: 0//ページトップまでスクロール
  }, 500);//ページトップスクロールの速さ。数字が大きいほど遅くなる
  return false;//リンク自体の無効化
});



//ハンバーガーボタン
$(".openbtn").click(function () {//ボタンがクリックされたら
	$(this).toggleClass('active');//ボタン自身に activeクラスを付与し
    $("#g-nav").toggleClass('panelactive');//ナビゲーションにpanelactiveクラスを付与
});

$("#g-nav a").click(function () {//ナビゲーションのリンクがクリックされたら
    $(".openbtn").removeClass('active');//ボタンの activeクラスを除去し
    $("#g-nav").removeClass('panelactive');//ナビゲーションのpanelactiveクラスも除去
});

//topへ戻るレスポンシブ
//スクロールした際の動きを関数でまとめる
function PageTopAnime() {
	var scroll = $(window).scrollTop();
	if (scroll >= 500){//上から500pxスクロールしたら
		$('#page-top2').removeClass('DownMove');//#page-top2についているDownMoveというクラス名を除く
		$('#page-top2').addClass('UpMove');//#page-top2についているUpMoveというクラス名を付与
	}else{
		if($('#page-top2').hasClass('UpMove')){//すでに#page-top2にUpMoveというクラス名がついていたら
			$('#page-top2').removeClass('UpMove');//UpMoveというクラス名を除き
			$('#page-top2').addClass('DownMove');//DownMoveというクラス名を#page-topに付与
		}
	}
}

// 画面をスクロールをしたら動かしたい場合の記述
$(window).scroll(function () {
	PageTopAnime();/* スクロールした際の動きの関数を呼ぶ*/
});

// ページが読み込まれたらすぐに動かしたい場合の記述
$(window).on('load', function () {
	PageTopAnime();/* スクロールした際の動きの関数を呼ぶ*/
});

